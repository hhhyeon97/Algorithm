// 사칙연산

// 두 수의 합
class Solution {
    public int solution(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }
}
// 두 수의 차
class Solution1 {
    public int solution(int num1, int num2) {
        int num = num1-num2;
        return num;
    }
}

// 두 수의 곱
class Solution3 {
    public int solution(int num1, int num2) {
        int num = num1*num2;
        return num;
    }
}

// 몫 구하기
class Solution4 {
    public int solution(int num1, int num2) {
        int answer = num1/num2;
        return answer;
    }
}